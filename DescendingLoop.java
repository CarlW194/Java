/* Algorithm
 *
 *1. declare variables
 *2. start the number from 100
 *3. use a while-loop operation to get the number to the number 2 in descending order
 *
 */
 public class DescendingLoop
 {
 	public static void main(String args[])
 	{
 		// declare variables
 		int count = 100;

 		// while loop statement
 		while (count>=2)
 		{
 		System.out.println(" Count is " + count);
 		count=count-2;
 		}
  	}
 }
